void main() {
  // Declaring and initialising a variable
  int gfg1 = 10;
  
  // Declaring another variable
  double gfg2 = 23.7;
  bool gfg3 = false;
  
  // Declaring multiple variable
  String gfg4 = "This is", gfg5 = "multiple variable declaration";
  
  // Printing values of all the variables
  print(gfg1); // Print 10
  print(gfg2); // Print 23.7
  print(gfg3); // Print false
  print(gfg4); // Print This is
  print(gfg5); // Print multiple variable declaration
}
